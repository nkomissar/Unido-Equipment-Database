DROP TRIGGER IF EXISTS add_topic_search
//
DROP TRIGGER IF EXISTS update_topic_search
//
DROP TRIGGER IF EXISTS delete_topic_search
//

CREATE TRIGGER add_topic_search AFTER INSERT ON UNIDO_TOPIC
  FOR EACH ROW 
  BEGIN
    INSERT INTO UNIDO_TOPIC_SEARCH 
	   SET TOPIC_ID = NEW.TOPIC_ID
         , TOPIC_NAME = NEW.TOPIC_NAME
         , DESCRIPTION = NEW.DESCRIPTION;
  END
//

CREATE TRIGGER update_topic_search BEFORE UPDATE ON UNIDO_TOPIC
  FOR EACH ROW 
  BEGIN
    UPDATE UNIDO_TOPIC_SEARCH 
	   SET TOPIC_NAME = NEW.TOPIC_NAME
         , DESCRIPTION = NEW.DESCRIPTION
     WHERE TOPIC_ID = OLD.TOPIC_ID;
  END;
//

CREATE TRIGGER delete_topic_search BEFORE DELETE ON UNIDO_TOPIC
  FOR EACH ROW 
  BEGIN
    DELETE
	  FROM UNIDO_TOPIC_SEARCH 
     WHERE TOPIC_ID = OLD.TOPIC_ID;
  END;
//

