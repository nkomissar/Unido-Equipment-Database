CREATE TRIGGER add_entity_property_search AFTER INSERT ON UNIDO_ENTITY_PROPERTY
  FOR EACH ROW 
  BEGIN
    UPDATE UNIDO_ENTITY_SEARCH 
	   SET DESCRIPTION = NEW.`VALUE`
	 WHERE ENTITY_ID = NEW.ENTITY_ID
	   AND NEW.TEMPLATE_PROPERTY_ID IN (SELECT TEMPLATE_PROPERTY_ID
										  FROM UNIDO_ENTITY_TEMPLATE_PROPERTY
										 WHERE PROPERTY_CODE = 'DESCRIPTION');
  END
//

CREATE TRIGGER update_entity_property_search BEFORE UPDATE ON UNIDO_ENTITY_PROPERTY
  FOR EACH ROW 
  BEGIN
    UPDATE UNIDO_ENTITY_SEARCH 
	   SET DESCRIPTION = NEW.`VALUE`
     WHERE ENTITY_ID = OLD.ENTITY_ID
	   AND OLD.TEMPLATE_PROPERTY_ID IN (SELECT TEMPLATE_PROPERTY_ID
										  FROM UNIDO_ENTITY_TEMPLATE_PROPERTY
										 WHERE PROPERTY_CODE = 'DESCRIPTION');
  END
//

CREATE TRIGGER delete_entity_property_search BEFORE DELETE ON UNIDO_ENTITY_PROPERTY
  FOR EACH ROW 
  BEGIN
    UPDATE UNIDO_ENTITY_SEARCH 
	   SET DESCRIPTION = NULL
     WHERE ENTITY_ID = OLD.ENTITY_ID
	   AND OLD.TEMPLATE_PROPERTY_ID IN (SELECT TEMPLATE_PROPERTY_ID
										  FROM UNIDO_ENTITY_TEMPLATE_PROPERTY
										 WHERE PROPERTY_CODE = 'DESCRIPTION');
  END
